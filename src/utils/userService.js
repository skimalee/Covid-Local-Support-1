import tokenService from "./tokenService";
import axios from "axios";

const BASE_URL = "/api/users/";

function signup(user) {
  // console.log("hitting user: ", user);
  return (
    fetch(BASE_URL + "signup", {
      method: "POST",
      headers: new Headers({ "Content-Type": "application/json" }),
      body: JSON.stringify(user),
    })
      .then((res) => {
        // console.log("this is res: ", res);
        if (res.ok) return res.json();
        // Probably a duplicate email
        throw new Error("Email already taken!");
      })
      // Parameter destructuring!
      .then(({ token }) => tokenService.setToken(token))
  );
  // The above could have been written as
  //.then((token) => token.token);
}

function addPost(query) {
  // console.log("hitting add location");
  return fetch(BASE_URL + "addpost", {
    method: "POST",
    headers: new Headers({ "Content-Type": "application/json" }),
    body: JSON.stringify(query),
  }).then((res) => {
    console.log("this is res from addPost", res);
    if (res.ok) return res.json();
    throw new Error("Add addPost Failed! Check userService.addPost path");
  });
}

function getUser() {
  return tokenService.getUserFromToken();
}

async function getUserByEmail(email) {
  try {
    const { data } = await axios.post(`${BASE_URL}getByEmail`, { email });
    return data;
  } catch (err) {
    console.error(err);
    return;
  }
}

function logout() {
  tokenService.removeToken();
}

function login(creds) {
  return fetch(BASE_URL + "login", {
    method: "POST",
    headers: new Headers({ "Content-Type": "application/json" }),
    body: JSON.stringify(creds),
  })
    .then((res) => {
      // Valid login if we have a status of 2xx (res.ok)
      if (res.ok) return res.json();
      throw new Error("Bad Credentials!");
    })
    .then(({ token }) => tokenService.setToken(token));
}

export default {
  signup,
  getUser,
  logout,
  login,
  getUserByEmail,
  addPost,
};
